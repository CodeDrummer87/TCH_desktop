(
	Id INT IDENTITY(1, 1) PRIMARY KEY,
	Title NVARCHAR(50) NOT NULL,
	Railroad INT NOT NULL,
	Code NVARCHAR(7) NOT NULL

	FOREIGN KEY (Railroad) REFERENCES Railroads(Id) ON DELETE CASCADE
)

